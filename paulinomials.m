function a=paulinomials(H)
%%
m{1}=[0 1 0;...
    1 0 0;...
    0 0 0];

m{2}=[0 -1i 0;...
    1i 0 0;...
    0 0 0];

m{3}=[1  0 0;...
    0 -1 0;...
    0 0 0];

m{4}=[0 0 1;...
    0 0 0;...
    1 0 0];

m{5}=[0  0 -1i;...
    0  0  0;...
    1i 0  0];

m{6}=[0 0 0;...
    0 0 1;...
    0 1 0];

m{7}=[0  0  0;...
    0  0 -1i;...
    0 1i  0];

m{8}=[1 0 0;...
    0 1 0;...
    0 0 -2]/sqrt(3);

m{9}=eye(3);

for i=1:length(m)
    for j=1:length(m)
        temp=kron(m{i},m{j});
        basis(:,sub2ind([length(m),length(m)],i,j))=temp(:);
    end
end

a=basis\H(:);

b=zeros(9);
for i=1:9
    for j=1:9
        b=b+a(sub2ind([length(m),length(m)],i,j))*kron(m{i},m{j});
    end
end

disp(H-b)
